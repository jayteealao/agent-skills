// lib/runtime-manifest.mjs
//
// Shared runtime identity — the single source of truth for "which runtime is
// this?" across BOTH host plugins (Claude `sdlc-workflow` and the native Codex
// `sdlc-workflow-codex`). See NATIVE-INTEROP-REWRITE-PLAN "Shared Runtime
// Identity" / Workstream B.
//
// The problem this fixes
// ----------------------
// Daemon adoption and render freshness used to key on the *plugin package*
// version (`package.json.version` / the `_shell.mjs` literal). That is correct
// for ONE package but wrong for two native plugins that must share one hub and
// one renderer runtime: their package versions legitimately differ, so a
// package-version reap rule makes Claude reap a healthy Codex hub and vice
// versa (Resolution 3 / Settled Decision 24 forbid exactly this).
//
// The fix is a host-NEUTRAL identity carried identically by both packages:
//   • runtimeVersion  — the shared renderer/hub/runtime release line. Both
//                       packages carry the SAME value for a given release.
//                       Hub *adoption* and the reap decision key on this.
//   • buildId         — sha256 over the shared runtime payload. Proves two
//                       packages carrying the same runtimeVersion contain the
//                       same runtime bytes. *Render freshness* (`.last-render`,
//                       stale-heal) keys on this.
//   • hubName/protocol/schema/registryVersion/hubConfigVersion — the rest of
//                       the compatibility surface.
//
// Plugin package versions remain host-specific metadata; nothing in the shared
// lifecycle keys on them anymore.
//
// `runtime-manifest.json` (committed at the plugin root) is GENERATED by the
// build (scripts/build.mjs) so runtimeVersion stays in lock-step with the
// package version and buildId always matches the freshly built dist/. This
// module reads it; when it is absent (a source tree before the first build) it
// derives a safe fallback from package.json so nothing crashes — buildId is
// null in that mode, which sorts as "no proof of identity" (stale-safe).

import { readFileSync } from 'node:fs';

// The machine-wide singleton hub name — host-independent, never derived from a
// package name. Both plugins name the same process so either can adopt it.
export const HUB_NAME = 'sdlc-workflow-hub';

// Bump this only on a genuine wire/behaviour break in the hub HTTP + lifecycle
// contract. A protocol mismatch (not a runtimeVersion mismatch) is what gates
// silent adoption (see hub-lifecycle "Hub Protocol Incompatible").
export const HUB_PROTOCOL_VERSION = 1;

// Compatibility-surface constants. Kept here (not re-derived) so the manifest
// generator and the fallback agree byte-for-byte.
export const ARTIFACT_SCHEMA = 'sdlc/v1';
export const REGISTRY_VERSION = 2;
export const HUB_CONFIG_VERSION = 1;
export const RUNTIME_FAMILY = 'sdlc-workflow';

// Resolved once. lib/runtime-manifest.mjs sits one level under the plugin root
// (lib/), and a bundled dist chunk also sits one level under it (the build's
// depth-1 invariant), so `../runtime-manifest.json` lands on the real
// plugin-root file identically from source and from dist.
const MANIFEST_URL = new URL('../runtime-manifest.json', import.meta.url);
const PACKAGE_URL = new URL('../package.json', import.meta.url);

let cached = null;

/**
 * Read the committed runtime manifest. Falls back to a package.json-derived
 * identity (buildId:null) when the manifest is absent or unparseable, so a
 * pre-build source tree never throws. Result is cached for the process.
 *
 * @returns {{ family:string, hubName:string, runtimeVersion:string,
 *   hubProtocolVersion:number, artifactSchema:string, registryVersion:number,
 *   hubConfigVersion:number, buildId:(string|null) }}
 */
export function readRuntimeManifest() {
  if (cached) return cached;
  cached = loadManifest();
  return cached;
}

/** Reset the process cache (tests that swap the manifest on disk). */
export function _resetRuntimeManifestCache() {
  cached = null;
}

function loadManifest() {
  try {
    const m = JSON.parse(readFileSync(MANIFEST_URL, 'utf-8'));
    return normalizeManifest(m);
  } catch {
    return fallbackManifest();
  }
}

function normalizeManifest(m) {
  const o = m && typeof m === 'object' ? m : {};
  return Object.freeze({
    family: typeof o.family === 'string' && o.family ? o.family : RUNTIME_FAMILY,
    hubName: typeof o.hubName === 'string' && o.hubName ? o.hubName : HUB_NAME,
    runtimeVersion: typeof o.runtimeVersion === 'string' && o.runtimeVersion ? o.runtimeVersion : readPackageVersion(),
    hubProtocolVersion: Number.isInteger(o.hubProtocolVersion) ? o.hubProtocolVersion : HUB_PROTOCOL_VERSION,
    artifactSchema: typeof o.artifactSchema === 'string' && o.artifactSchema ? o.artifactSchema : ARTIFACT_SCHEMA,
    registryVersion: Number.isInteger(o.registryVersion) ? o.registryVersion : REGISTRY_VERSION,
    hubConfigVersion: Number.isInteger(o.hubConfigVersion) ? o.hubConfigVersion : HUB_CONFIG_VERSION,
    buildId: typeof o.buildId === 'string' && o.buildId ? o.buildId : null,
  });
}

function fallbackManifest() {
  return Object.freeze({
    family: RUNTIME_FAMILY,
    hubName: HUB_NAME,
    runtimeVersion: readPackageVersion(),
    hubProtocolVersion: HUB_PROTOCOL_VERSION,
    artifactSchema: ARTIFACT_SCHEMA,
    registryVersion: REGISTRY_VERSION,
    hubConfigVersion: HUB_CONFIG_VERSION,
    buildId: null,
  });
}

function readPackageVersion() {
  try { return JSON.parse(readFileSync(PACKAGE_URL, 'utf-8')).version ?? ''; }
  catch { return ''; }
}

/**
 * The active runtime's identity, as the small object consumers compare against.
 * `{ runtimeVersion, buildId, hubName, hubProtocolVersion }`.
 */
export function runtimeIdentity() {
  const m = readRuntimeManifest();
  return {
    runtimeVersion: m.runtimeVersion,
    buildId: m.buildId,
    hubName: m.hubName,
    hubProtocolVersion: m.hubProtocolVersion,
  };
}

/**
 * Read the runtime identity recorded in a view's `.last-render` marker.
 * Returns `{ version, buildId }` (each null when absent/torn/legacy). A pre-9.75
 * marker has `version` but no `buildId`; a pre-9.60 marker has neither — both
 * sort as "no buildId proof", handled by renderIdentityMatches.
 */
export function readRenderedIdentity(markerPath) {
  try {
    const parsed = JSON.parse(readFileSync(markerPath, 'utf-8'));
    return {
      version: typeof parsed.version === 'string' && parsed.version ? parsed.version : null,
      buildId: typeof parsed.buildId === 'string' && parsed.buildId ? parsed.buildId : null,
    };
  } catch {
    return { version: null, buildId: null };
  }
}

/**
 * Does a recorded render identity match the active runtime?
 *
 * Precedence (the migration-safe rule):
 *   1. If BOTH the marker and the active runtime carry a buildId, compare
 *      buildId — the precise signal (catches a same-version rebuild).
 *   2. Otherwise fall back to runtimeVersion-vs-recorded-version — so a legacy
 *      marker (version only) still heals correctly against a bumped runtime,
 *      and an active runtime with no buildId (a pre-build source tree) still
 *      compares on version.
 *
 * A null/empty on the deciding axis is a non-match (stale) — unversioned
 * content is exactly the split-brain the heal repairs.
 *
 * @param {{version:(string|null), buildId:(string|null)}} recorded
 * @param {{runtimeVersion:(string|null), buildId:(string|null)}} active
 * @returns {boolean}
 */
export function renderIdentityMatches(recorded, active) {
  const r = recorded ?? {};
  const a = active ?? {};
  if (r.buildId && a.buildId) return r.buildId === a.buildId;
  return Boolean(r.version) && r.version === a.runtimeVersion;
}
