// lib/tray-autostart.mjs
//
// Opt-in, per-user logon autostart for the tray (docs/internal/archived/TRAY-APP-PLAN.md → "Autostart
// (P5)"). No admin, no service/registry, no Task Scheduler: a generated launcher
// file in the user's per-user autostart location whose PRESENCE is the on/off
// truth. All three platforms share one shape — materialize / remove / detect a
// launcher file — so this module is a thin dispatch with pure content builders.
//
//   Windows : %APPDATA%\…\Startup\SDLC Sunflower Tray.vbs   (hidden-window .vbs)
//   macOS   : ~/Library/LaunchAgents/com.sdlc.sunflower-tray.plist  (RunAtLoad)
//   Linux   : ~/.config/autostart/sdlc-sunflower-tray.desktop       (XDG)
//
// Embedded paths are ABSOLUTE and captured at enable-time (process.execPath +
// the tray bundle's own path) — at logon there is no guarantee `node` is on a GUI
// session's PATH. `refreshAutostart` self-heals a launcher left pointing at a
// relocated bundle after a plugin upgrade.
//
// Every fs/OS seam is injectable so the whole module is unit-testable against a
// temp dir without touching the real Startup folder.

import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { homedir } from 'node:os';
import { join } from 'node:path';

const LABEL = 'com.sdlc.sunflower-tray';
const APP_NAME = 'SDLC Sunflower Tray';

/* ───────────────────────── location + name ───────────────────────── */

/** Per-user autostart directory for the platform. */
export function autostartLauncherDir({ platform = process.platform, env = process.env, home = homedir() } = {}) {
  if (platform === 'win32') {
    const appData = env.APPDATA || join(home, 'AppData', 'Roaming');
    return join(appData, 'Microsoft', 'Windows', 'Start Menu', 'Programs', 'Startup');
  }
  if (platform === 'darwin') return join(home, 'Library', 'LaunchAgents');
  const xdg = env.XDG_CONFIG_HOME || join(home, '.config');
  return join(xdg, 'autostart');
}

/** Launcher filename for the platform. */
export function autostartLauncherName(platform = process.platform) {
  if (platform === 'win32') return `${APP_NAME}.vbs`;
  if (platform === 'darwin') return `${LABEL}.plist`;
  return 'sdlc-sunflower-tray.desktop';
}

/** Absolute path to the launcher file. `startupDir` overrides the computed dir. */
export function autostartLauncherPath({ platform = process.platform, env = process.env, home = homedir(), startupDir } = {}) {
  const dir = startupDir ?? autostartLauncherDir({ platform, env, home });
  return join(dir, autostartLauncherName(platform));
}

/* ───────────────────────── pure content builders ───────────────────────── */

/** The generated launcher file body for the platform (pure). */
export function buildLauncherContent({ platform = process.platform, nodePath, trayBundle } = {}) {
  if (!nodePath || !trayBundle) throw new Error('buildLauncherContent requires nodePath and trayBundle');
  if (platform === 'win32') return winVbs(nodePath, trayBundle);
  if (platform === 'darwin') return macPlist(nodePath, trayBundle);
  return linuxDesktop(nodePath, trayBundle);
}

function winVbs(nodePath, trayBundle) {
  // VBS escapes a literal " as "". The Run argument resolves to:
  //   "<node>" "<bundle>"
  // launched with window-style 0 (hidden) and wait=False (fire-and-forget).
  const runArg = `"""${nodePath}"" ""${trayBundle}"""`;
  return [
    `' ${APP_NAME} — autostart launcher (generated by lib/tray-autostart.mjs; do not edit).`,
    "' Presence of this file in the Startup folder = Start-at-login is ON.",
    "' Hidden-window launch (style 0, no-wait) so there is no console flash at logon.",
    'Dim sh',
    'Set sh = CreateObject("WScript.Shell")',
    `sh.Run ${runArg}, 0, False`,
    '',
  ].join('\r\n');
}

function macPlist(nodePath, trayBundle) {
  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">',
    '<plist version="1.0">',
    '<dict>',
    '  <key>Label</key>',
    `  <string>${LABEL}</string>`,
    '  <key>ProgramArguments</key>',
    '  <array>',
    `    <string>${nodePath}</string>`,
    `    <string>${trayBundle}</string>`,
    '  </array>',
    '  <key>RunAtLoad</key>',
    '  <true/>',
    '</dict>',
    '</plist>',
    '',
  ].join('\n');
}

function linuxDesktop(nodePath, trayBundle) {
  return [
    '[Desktop Entry]',
    'Type=Application',
    `Name=${APP_NAME}`,
    `Exec="${nodePath}" "${trayBundle}"`,
    'X-GNOME-Autostart-enabled=true',
    'Terminal=false',
    '',
  ].join('\n');
}

/** Pure: does the launcher body already reference the current node + bundle paths? */
export function launcherTargetsCurrent(content, { nodePath, trayBundle } = {}) {
  const s = String(content ?? '');
  return Boolean(nodePath) && Boolean(trayBundle) && s.includes(nodePath) && s.includes(trayBundle);
}

/* ───────────────────────── durable node resolution ───────────────────────── */

/** True when a node path lives in fnm's per-shell ephemeral multishell dir. */
function isFnmMultishellPath(p) {
  return /fnm_multishells/i.test(String(p ?? ''));
}

/** fnm's base dir (holds aliases/ + node-versions/). $FNM_DIR wins; else the OS default. */
function fnmBaseDir({ platform = process.platform, env = process.env, home = homedir() } = {}) {
  if (env.FNM_DIR) return env.FNM_DIR;
  if (platform === 'win32') return join(env.APPDATA || join(home, 'AppData', 'Roaming'), 'fnm');
  if (platform === 'darwin') return join(home, 'Library', 'Application Support', 'fnm');
  return join(env.XDG_DATA_HOME || join(home, '.local', 'share'), 'fnm');
}

/**
 * A STABLE node binary path to embed in the launcher. process.execPath is fragile
 * under fnm: inside an `fnm use` shell it points into a per-shell `fnm_multishells/…`
 * dir that fnm deletes on shell exit, so a launcher embedding it silently dies at the
 * next logon (the very failure this heals). When execPath is such an ephemeral path,
 * swap in fnm's durable `aliases/default` node (which fnm re-points across version
 * switches); otherwise execPath is already a real install and is kept. Falls back to
 * execPath when no durable candidate exists — never worse than before. Pure; every
 * seam injectable.
 */
export function resolveDurableNodePath({
  execPath = process.execPath, platform = process.platform,
  env = process.env, home = homedir(), exists = existsSync,
} = {}) {
  if (!isFnmMultishellPath(execPath)) return execPath;
  const base = fnmBaseDir({ platform, env, home });
  if (!base) return execPath;
  const candidate = platform === 'win32'
    ? join(base, 'aliases', 'default', 'node.exe')
    : join(base, 'aliases', 'default', 'bin', 'node');
  return exists(candidate) ? candidate : execPath;
}

/* ───────────────────────── enable / disable / detect ───────────────────────── */

/** Is the launcher present (file presence = state)? */
export function isAutostartEnabled({
  platform = process.platform, env = process.env, home = homedir(), startupDir, exists = existsSync,
} = {}) {
  return exists(autostartLauncherPath({ platform, env, home, startupDir }));
}

/** Write the launcher (turn autostart ON). Returns { path, content }. */
export function enableAutostart({
  platform = process.platform, env = process.env, home = homedir(), startupDir,
  nodePath = resolveDurableNodePath({ platform, env, home }), trayBundle,
  mkdir = mkdirSync, writeFile = writeFileSync,
} = {}) {
  const dir = startupDir ?? autostartLauncherDir({ platform, env, home });
  const path = join(dir, autostartLauncherName(platform));
  const content = buildLauncherContent({ platform, nodePath, trayBundle });
  mkdir(dir, { recursive: true });
  writeFile(path, content, 'utf-8');
  return { path, content };
}

/** Remove the launcher (turn autostart OFF). Idempotent. Returns { path, removed }. */
export function disableAutostart({
  platform = process.platform, env = process.env, home = homedir(), startupDir, rm = rmSync,
} = {}) {
  const path = autostartLauncherPath({ platform, env, home, startupDir });
  rm(path, { force: true });
  return { path, removed: true };
}

/**
 * Self-heal: if autostart is enabled but the launcher points at a stale bundle
 * (e.g. a plugin upgrade relocated dist/tray.cjs), rewrite it to the current
 * paths. No-op when disabled or already current. Returns { action, path }.
 */
export function refreshAutostart({
  platform = process.platform, env = process.env, home = homedir(), startupDir,
  nodePath = resolveDurableNodePath({ platform, env, home }), trayBundle,
  exists = existsSync, readFile = readFileSync, writeFile = writeFileSync, mkdir = mkdirSync,
} = {}) {
  const dir = startupDir ?? autostartLauncherDir({ platform, env, home });
  const path = join(dir, autostartLauncherName(platform));
  if (!exists(path)) return { action: 'disabled', path };
  let current = '';
  try { current = readFile(path, 'utf-8'); } catch { current = ''; }
  if (launcherTargetsCurrent(current, { nodePath, trayBundle })) return { action: 'unchanged', path };
  const content = buildLauncherContent({ platform, nodePath, trayBundle });
  mkdir(dir, { recursive: true });
  writeFile(path, content, 'utf-8');
  return { action: 'rewritten', path };
}
